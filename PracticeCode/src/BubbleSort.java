public class BubbleSort {

    static int[] bubbleSort(int[] arr) {
        int n = arr.length;
        int temp = 0;
        for (int i = 0; i < n; i++) {
            for (int j = 1; j < (n - i); j++) {
                if (arr[j - 1] > arr[j]) {
                    temp = arr[j - 1];
                    arr[j - 1] = arr[j];
                    arr[j] = temp;
                }
            }
        }
        return arr;
    }

    static int[] bubbleSort2(int[] arr) {
        int n = arr.length;
        boolean changed = true;
        while(changed){
            changed = false;
            for (int i = 0; i < n - 1; i++) {
                int temp = 0;
                if (arr[i] > arr[i + 1]) {
                    temp = arr[i];
                    arr[i] = arr[i + 1];
                    arr[i + 1] = temp;
                    changed = true;
                }
            }
        }
        return arr;
    }
}
